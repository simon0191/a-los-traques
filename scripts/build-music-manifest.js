#!/usr/bin/env node
/**
 * Build-time replacement for the Vite `import.meta.glob('/public/assets/audio/fights/*.mp3')`
 * trick. Reads `apps/web/public/assets/audio/fights/` and emits
 * `packages/game/src/data/music-manifest.js` with a static array of URL paths
 * the Phaser loader can consume at runtime.
 *
 * Wired into `apps/web`'s build via a predev / prebuild script; idempotent,
 * cheap, no Next/Vite coupling.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const musicDir = path.join(repoRoot, 'apps/web/public/assets/audio/fights');
const manifestPath = path.join(repoRoot, 'packages/game/src/data/music-manifest.js');

function readTracks() {
  try {
    return fs
      .readdirSync(musicDir)
      .filter((name) => name.endsWith('.mp3'))
      .sort();
  } catch (err) {
    if (err.code === 'ENOENT') return [];
    throw err;
  }
}

function renderManifest(tracks) {
  // Paths are root-relative WITHOUT a leading slash — BootScene calls
  // `this.load.setBaseURL('/')` before preloading, and Phaser's loader
  // concatenates base + path. A leading slash here + a base ending in `/`
  // produces `//assets/...`, which browsers interpret as a protocol-relative
  // URL (host = "assets") and fail to resolve DNS for.
  const entries = tracks.map((name) => `  'assets/audio/fights/${name}',`).join('\n');
  return `// AUTO-GENERATED by scripts/build-music-manifest.js — do not edit by hand.
// Sources: apps/web/public/assets/audio/fights/*.mp3

export const FIGHT_MUSIC_FILES = Object.freeze([
${entries}${entries ? '\n' : ''}]);
`;
}

function main() {
  const tracks = readTracks();
  const content = renderManifest(tracks);
  fs.mkdirSync(path.dirname(manifestPath), { recursive: true });
  fs.writeFileSync(manifestPath, content, 'utf-8');
  console.log(
    `[music-manifest] wrote ${tracks.length} track${tracks.length === 1 ? '' : 's'} -> ${path.relative(repoRoot, manifestPath)}`,
  );
}

main();
